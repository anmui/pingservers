package dao;

public class UserInformation {

	
	private String v_nickname;
	private String v_dormnumber;
	private String v_motto;
	private String v_grade;
	private String v_sex;
	private String v_major;
	public String getV_nickname() {
		return v_nickname;
	}
	public void setV_nickname(String v_nickname) {
		this.v_nickname = v_nickname;
	}
	public String getV_dormnumber() {
		return v_dormnumber;
	}
	public void setV_dormnumber(String v_dormnumber) {
		this.v_dormnumber = v_dormnumber;
	}
	public String getV_motto() {
		return v_motto;
	}
	public void setV_motto(String v_motto) {
		this.v_motto = v_motto;
	}
	public String getV_grade() {
		return v_grade;
	}
	public void setV_grade(String v_grade) {
		this.v_grade = v_grade;
	}
	public String getV_sex() {
		return v_sex;
	}
	public void setV_sex(String v_sex) {
		this.v_sex = v_sex;
	}
	public String getV_major() {
		return v_major;
	}
	public void setV_major(String v_major) {
		this.v_major = v_major;
	}

	
}
